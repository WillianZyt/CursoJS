/** 
 * Aritmeticos
 * +  Adicao / Contatenacao  (-  /  *)
 * ** Potenciacao
 * % Resto da divisao
 * Ordem das operacoes, nessa ordem -> () , ** , * / % , + -
*/
let contador = 1; // ++ soma 1
contador++; // 2
contador++; // 3
++contador; // 4 primeiro incrementa o valor, sem seguida mostra o valor
console.log(contador);

